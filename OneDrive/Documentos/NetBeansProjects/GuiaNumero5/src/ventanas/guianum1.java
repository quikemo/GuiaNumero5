    private void jButtonCalculoEsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalculoEsActionPerformed
        if ((jTextFieldValoringresadoEs.getText().length() != 0)) {
            if (jRadioButtonRadioEs.isSelected() || jRadioButtonDiametroEs.isSelected()) {
                double valorIngresado;
                double resultado;
                String res;
                valorIngresado = Double.parseDouble(jTextFieldValoringresadoEs.getText());
                if (jRadioButtonDiametroEs.isSelected()) {
                    valorIngresado = valorIngresado / 2;
                    valorIngresado = Math.pow(valorIngresado, 3);
                    resultado = (1.33333) * 3.141559 * (valorIngresado);
                    DecimalFormat df = new DecimalFormat("#.00");
                    res = df.format(resultado);
                    jLabelResVolumenEs.setText(res);
                } else if (jRadioButtonRadioEs.isSelected()) {
                    valorIngresado = Math.pow(valorIngresado, 3);
                    resultado = (1.33333) * 3.14159 * (valorIngresado);
                    DecimalFormat df = new DecimalFormat("#.00");
                    res = df.format(resultado);
                    jLabelResVolumenEs.setText(res);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar si es radio o diametro ese valor no puede quedar vacio");
            }
        }else {
                JOptionPane.showMessageDialog(null, "El campo no puede estar vacio");
        }
    }//GEN-LAST:event_jButtonCalculoEsActionPerformed

    private void jButtonCalculoCuboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalculoCuboActionPerformed
        if ((jTextFieldBase.getText().length() != 0) && (jTextFieldAltura.getText().length() != 0) && (jTextFieldProfundidad.getText().length() != 0)) {
            double base;
            double altura;
            double profundidad;
            Double respuesta;
            String res;
            base = Double.parseDouble(jTextFieldBase.getText());
            altura = Double.parseDouble(jTextFieldAltura.getText());
            profundidad = Double.parseDouble(jTextFieldProfundidad.getText());
            respuesta = base * altura * profundidad;
            DecimalFormat df = new DecimalFormat("#.00");
            res = df.format(respuesta);
            jLabelResVol.setText(res);

        } else {
            JOptionPane.showMessageDialog(null, "El campo no puede estar vacio");
        }
    }//GEN-LAST:event_jButtonCalculoCuboActionPerformed

