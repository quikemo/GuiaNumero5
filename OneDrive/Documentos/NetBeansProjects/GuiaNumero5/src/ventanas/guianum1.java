    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        if ((jTextLado2.getText().length() != 0) && (jTextLado1.getText().length() != 0)) {

            jLabelResDiagonal.setText("");
            jLabelCubolado1.setText("");
            jLabelCubolado2.setText("");
            jLabelResArea.setText("");
            double lado1;
            double lado2;
            double resul;
            double num;
            double resdiagonal;
            String res;
            String resdiagona;
            lado1 = Double.parseDouble(jTextLado1.getText());
            lado2 = Double.parseDouble(jTextLado2.getText());
            jLabelCubolado1.setText("" + lado1);
            jLabelCubolado2.setText("" + lado2);
            resul = lado1 * lado2;
            DecimalFormat df = new DecimalFormat("#.00");
            res = df.format(resul);
            jLabelResArea.setText("" + res);
            num = (double) ((Math.pow(lado1, 2)) + (Math.pow(lado2, 2)));
            resdiagonal = Math.sqrt(num);
            resdiagona = df.format(resdiagonal);
            jLabelResDiagonal.setText("" + resdiagona);
            jTextLado1.setText("");
            jTextLado2.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "los campos no pueden estar vacios");
        }
    }//GEN-LAST:event_jButtonCalcularActionPerformed

