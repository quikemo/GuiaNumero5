    private void jButtonCalcularResActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularResActionPerformed
        if ((jTextFieldValorIngresado.getText().length() != 0)) {
            jLabelResCircunferencia.setText("");
            if (jRadioButtonRadio.isSelected() || jRadioButtonDiametro.isSelected()) {
                double valorIngresado;
                double resultado;
                String res;
                valorIngresado = Double.parseDouble(jTextFieldValorIngresado.getText());
                if (jRadioButtonDiametro.isSelected()) {
                    resultado = 3.14 * valorIngresado;
                    DecimalFormat df = new DecimalFormat("#.00");
                    res = df.format(resultado);
                    jLabelResCircunferencia.setText(res);
                } else if (jRadioButtonRadio.isSelected()) {
                    resultado = 3.14 * (2 * valorIngresado);
                    DecimalFormat df = new DecimalFormat("#.00");
                    res = df.format(resultado);
                    jLabelResCircunferencia.setText(res);
                }
            } else {
                JOptionPane.showMessageDialog(null, "debe tener seleccionado ya sea 'Radio' o 'Diametro' \n no puede dejar vacio los dos botones");
            }
        } else {
            JOptionPane.showMessageDialog(null, "los campos no pueden estar vacios");
        }
    }//GEN-LAST:event_jButtonCalcularResActionPerformed

